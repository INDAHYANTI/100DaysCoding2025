public class KonstantaSederhana {
    public static void main(String[] args) {
        final double PI = 3.14;  // konstanta

        int r = 5; // jari-jari lingkaran
        double luas = PI * r * r;

        System.out.println("Jari-jari = " + r);
        System.out.println("Luas lingkaran = " + luas);
    }
}
