public class LuasLingkaran {
    public static void main(String[] args) {
        // Nilai jari-jari lingkaran
        double r = 14;  

        // Rumus luas = π × r × r
        double luas = Math.PI * r * r;  

        // Tampilkan hasil
        System.out.println("Jari-jari lingkaran = " + r);
        System.out.println("Luas lingkaran = " + luas);
    }
}
