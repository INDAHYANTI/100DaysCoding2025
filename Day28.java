public class OperatorPerbandingan {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;

        System.out.println("Nilai a = " + a);
        System.out.println("Nilai b = " + b);

        //Operator ==
        System.out.println (a == b);

        //Operator !=
        System.out.println (a != b);
    }
}
