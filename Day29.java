public class Perbandingan {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        // Menggunakan operator <
        if (a < b) {
            System.out.println(a + " lebih kecil dari " + b);
        }

        // Menggunakan operator >
        if (b > a) {
            System.out.println(b + " lebih besar dari " + a);
        }
    }
}
