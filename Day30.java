public class Day030 {
    public static void main(String[] args) {
        int a = 12;
        int b = 15;
        int c = 12;

        System.out.println(a + " >= " + b + " = " + (a >= b));
        System.out.println(a + " >= " + c + " = " + (a >= c));
        System.out.println(a + " <= " + b + " = " + (a <= b));
        System.out.println(a + " <= " + c + " = " + (a <= c));
    }
}
